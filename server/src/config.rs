    /// 清理环境变量
    ///
    /// 注意: env::remove_var 在 Rust 1.80+ 中标记为 unsafe
    fn cleanup_env_vars() {
        for (key, _) in env::vars() {
            if key.starts_with("APP__") {
                unsafe {
                    env::remove_var(&key);
                }
            }
        }
    }

    /// 环境变量测试的 RAII 守卫
    struct EnvVarGuard;

    impl EnvVarGuard {
        fn new() -> Self {
            cleanup_env_vars();
            EnvVarGuard
        }
    }

    impl Drop for EnvVarGuard {
        fn drop(&mut self) {
            cleanup_env_vars();
        }
    }

    /// 配置文件测试的 RAII 守卫
    struct ConfigFileGuard;

    impl ConfigFileGuard {
        fn new() -> Self {
            if std::path::Path::new("config.toml").exists() {
                std::fs::rename("config.toml", "config.toml.bak")
                    .expect("备份 config.toml 失败");
            }
            ConfigFileGuard
        }
    }

    impl Drop for ConfigFileGuard {
        fn drop(&mut self) {
            if std::path::Path::new("config.toml.bak").exists() {
                std::fs::rename("config.toml.bak", "config.toml")
                    .expect("恢复 config.toml 备份失败");
            }
        }
    }

